/**
 * פונקציה הממירה את נתוני השלב למטריצה (מערך דו-ממדי).
 * תומכת בפורמט grid (חדש) ובפורמט data (ישן).
 */
function getLevelGrid(level) {
    // אם כבר יש גריד מוכן, נחזיר אותו
    if (level.grid) {
        return level.grid;
    }
    
    // תמיכה לאחור בפורמט הסטרינג הישן (data)
    if (level.data) {
        const size = level.size || Math.sqrt(level.data.length);
        const matrix = [];
        for (let i = 0; i < size; i++) {
            const row = level.data.slice(i * size, (i + 1) * size).split('').map(Number);
            matrix.push(row);
        }
        return matrix;
    }
    
    return []; 
}

